package Percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("tetap semangat");
                break;
            case 2:
                System.out.println("menjalani hidup");
                break;
            case 3:
                System.out.println("meski dunia");
                break;
            case 4:
                System.out.println("tidak bersahabat");
                break;
            default:
                System.out.println("pada kita");
                break;
        }
    }
}
